/**************************************************************************/
/* Declare include files
 **************************************************************************/
#include <stdio.h>
#include <stdlib.h>

/**************************************************************************/
/* Defines
 **************************************************************************/
#define SIZE 100

/**************************************************************************/
/* Function prototypes
**************************************************************************/
int toggle( int a[ ] );
int state( int a[ ] );


/**************************************************************************
 * Main
 **************************************************************************/
int main( void )
{
	int ar[SIZE] = {0};

	toggle(ar);
	state(ar);

	return EXIT_SUCCESS;
}

/**************************************************************************
 * Function: int toggle( char a[ ] )
 **************************************************************************/
int toggle( int a[ ] )
{
	int i,j;
	for(i=1 ; i<=SIZE ; i++)
	{
		for(j=i; j<=SIZE ; j+=i)
		{
			if(a[j-1] == 0)
			{
				a[j-1] = 1;
			}
			else if(a[j-1] == 1)
			{
				a[j-1] = 0;
			}
		}
	}
	return 0;
}


/**************************************************************************
 * Function:  int state( char a[ ] )
 **************************************************************************/
int state( int a[ ] )
{
	int i;
	for(i=0 ; i<SIZE ; i++)
	{
		printf("%d ", a[i]);
	}
	return 0;
}
